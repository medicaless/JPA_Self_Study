        QItem qItem = QItem.item;
        JPAQuery<Item> query = queryFactory.selectFrom(qItem).where(qItem.itemSellStatus.eq(ItemSellStatus.SELL)).where(qItem.itemDetail.like("%"+"테스트 상품 상세설명"+"%")).orderBy(qItem.price.desc());
        List<Item> itemList = query.fetch();
